    private val generatedNames = mutableMapOf<String, Int>()

    fun generateName(random: Random): String {
        val chosen = names.random(random)
        val count = generatedNames[chosen] ?: 0
        generatedNames[chosen] = count + 1
        return if (count > 0) {
            "$chosen (${count + 1})"
        } else {
            chosen
        }
    }
